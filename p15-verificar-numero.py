# Verificar si un numero entero es postiivo, negativo o cero

print('Verificando si un numero entero es postiivo, negativo o cero ...')
numero = int(input('Dame un numero ? '))
if numero > 0:
    print('El numero es postivo')
if numero < 0:
    print('El numero es negativo')
if numero == 0: print('El numero es cero')